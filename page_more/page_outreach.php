      <p>For many years now, 1418 has "adopted" a rookie or younger team for a season to help them get into the swing of things, let them borrow our shop and tools, or to just lend a helping hand when it comes to robot construction. Some of these teams have included George Marshall 5549 and D.C.s School without Walls.</p>
